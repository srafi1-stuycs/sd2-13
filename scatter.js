function main() {
    var svg = d3.select('svg');
    svg.append('text').attr('x', 200).attr('y', 490).html('time [hours]');
    for (var i = 0; i < 4; i++) {
        svg.append('text').attr('x', 100*(i+1)).attr('y', 470).html(i);
    }
    svg.append('text').attr('x', 30).attr('y', 250).attr('transform', 'rotate(-90 30 250)').html('GPA');
    for (var i = 0; i < 5; i++) {
        svg.append('text').attr('y', 530 - 100*(i+1)).attr('x', 70).html(75 + 5*i);
    }

    for (var i = 0; i < 4; i++) {
        for (var j = 0; j < 5; j++) {
            var gpa = 75 + 5*j;
            var socialMedia = i;
            var r = countCombo(gpa, socialMedia);
            svg.append('circle').attr('cx', 100*(i+1)).attr('cy', 530 - 100*(j+1)).attr('r', r);
        }
    }
}

function countCombo(gpa, socialMedia) {
    var count = 0;
    for (var i = 0; i < data.length; i++) {
        if (data[i][0] == gpa && data[i][1] == socialMedia) {
            count++;
        }
    }
    return count;
}
